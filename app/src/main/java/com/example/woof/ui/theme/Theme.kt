private val LightColorPalette = lightColorScheme(
    background = Green100,
    surface = Green50,
    onSecondary = Green25,
    onSurface = Grey900,
    primary = Grey50,
    onPrimary = Grey900,
    secondary = Grey700
)

private val DarkColorPalette = darkColorScheme(
    background = Cyan900,
    surface = Cyan700,
    onSecondary = Cyan500,
    onSurface = White,
    primary = Grey900,
    onPrimary = White,
    secondary = Grey100
)

@Composable
fun WoofTheme(darkTheme: Boolean = isSystemInDarkTheme(), content: @Composable () -> Unit) {
    val colors = if (darkTheme) {
        DarkColorPalette
    } else {
        LightColorPalette
    }

    MaterialTheme(
        colorScheme = colors,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}